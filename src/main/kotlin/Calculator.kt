class Calculator {
    fun addOperate(num1: Long, num2: Long): Long {
        return (num1 + num2)
    }
    fun subtractOperate(num1: Long, num2: Long): Long {
        return (num1 - num2)
    }
    fun multiplyOperate(num1: Long, num2: Long): Long {
        return (num1 * num2)
    }
    fun divideOperate(num1: Long, num2: Long): Long {
        return (num1 / num2)
    }
    fun remainderOperate(num1: Long, num2: Long): Long {
        return (num1 % num2)
    }
}
